/*
1、dataset
    使用data-前缀设置需要的自定义属性
    不需要任何循环去获取你想要的那个值
    和getAttribute相似，比其慢些，但操作行、可读性更高
    ================================================
2、play() 方法
    开始播放当前的音频或视频。
    该方法通常与 pause() 方法一起使用。
    function playVid()
    {
      x.play();
    }    
    =================================================
3、DOM classList 属性
    该属性用于在元素中添加，移除及切换 CSS 类。
    classList 属性是只读的，但可以使用 add() 和 remove() 方法修改    
*/